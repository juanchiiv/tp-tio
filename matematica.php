<?php
/**
 * Libreria con funciones matematicas
 */

function sumar($x, $y) {
    $resultado = $x + $y;

    return $resultado;
}

function restar($x, $y) {
    $resultado = $x - $y;

    return $resultado;
}

/**
 * Multiplica los dos operandos
 */
function multiplicar($x, $y) {
    $resultado = $x * $y;

    return $resultado;
}

function dividir($x, $y) {
    $resultado = $x / $y;

    return $resultado;
}

function valorPi() {
    return pi();
}